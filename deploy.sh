What is the differences between bin and sbin in Linux?
Ans: Both bin and sbin they contain binary files. However the binary files in bin can be 
     executed by every user in the server but the binary files in sbin can be executed
     only by users with admin privileges.
- Commands in Linux are in lowercases.
- Linux is case sensitive = test / Test / TEST are three different files in Linux
  unlike in Windows which are the same file.
- Directories starts with D and appears in BLUE while files starts with a "-"
  and appears in white. 
